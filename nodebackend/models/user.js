var mongoose = require('mongoose');

var UserSchema = mongoose.Schema({
   
    name: {
        type: String,
        required: true
    },
    age: {
        type: Number,
        required: true
    },
    gender: {
        type: String,
        required: true
    },
    weight: {
        type: Number,
        required: true
    },
    height: {
        type: Number,
        required: true
    }
});

var User = module.exports = mongoose.model("User", UserSchema);